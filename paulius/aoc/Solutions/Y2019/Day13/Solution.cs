        await inputTask;
        //Console.ReadLine();
        var result = await displayTask;
        return result.Item2;
    }

    private static async Task<(int, int)> Display(ChannelReader<long> instructions)
    {
        await Task.Yield();
        var initialTop = Console.CursorTop;
        var inititalLeft = Console.CursorLeft;

        var blockCount = 0;

        var score = 0;

        var screen = new char[25][].Select(a => new char[50]).ToArray();

        var channel = Channel.CreateBounded<string>(new BoundedChannelOptions(1)
        {
            FullMode = BoundedChannelFullMode.DropOldest
        });

        var writeLineTask = Task.Run(async () =>
        {
            await Task.Yield();
            while (await channel.Reader.WaitToReadAsync())
            {
                var toDraw = await channel.Reader.ReadAsync();
                Console.SetCursorPosition(inititalLeft, initialTop);
                Console.WriteLine(toDraw);
            }
        });

        while ( await instructions.WaitToReadAsync())
        {
            var x = (int)await instructions.ReadAsync();
            var y = (int)await instructions.ReadAsync();
            var t = (int)await instructions.ReadAsync();

            if ((x, y) != (-1, 0))
            {
                var tChar = t switch
                {
                    1 => '#',
                    2 => 'x',
                    3 => '-',
                    4 => 'o',
                    _ => ' '
                };
                screen[y][x] = tChar;
                if (t is 2) blockCount++;
            }
            else
            {
                score = t;
            }

            var toDraw = score.ToString().PadLeft(10) + "\n" + string.Join('\n', from row in screen
                                                                                 select new string(row));
            await channel.Writer.WriteAsync(toDraw);
        }
        channel.Writer.Complete();
        return (blockCount, score);
    }

