    public async Task<object> Part1Async(Input input)
    {
        var memory = new IntCode.Memory<long>(input.Select(v => new MemCell<long> { Value = long.Parse(v) }).ToArray());

        var brain = new ProcessingUnit(memory);
        var brainTask = brain.Run();
        brain.IN.Complete();
        await brainTask;

        var result = await Display(brain.OUT);
        return result.Item1;
    }

    public async Task<object> Part2Async(Input input)
